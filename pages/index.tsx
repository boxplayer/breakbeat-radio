import type { NextPage } from "next";
import { useEffect, useState } from "react";
import Head from "next/head";
import styles from "../styles/Home.module.css";
import { YouTubeEmbed } from "../components/YouTubeEmbed";
import { MediaButtons } from "../components/MediaButtons";
import { SocialMediaButtons } from "../components/SocialMediaButtons";
import { NowPlaying } from "../components/NowPlaying";

export const breakIds = [
  "8CHva3gBp68&t",
  "zSCzORZQsjQ",
  "A9UW7i6Pj3Q",
  "Owldd4hs7wQ&t",
];

export const fetchData = async () => {
  const metadata = await fetch(
    "https://noembed.com/embed?url=https%3A%2F%2Fwww.youtube.com%2Fwatch%3Fv%3DyEuXlaXdWhs",
  );
  return metadata;
};

const Home: NextPage = () => {
  const [introMessage, setIntroMessage] = useState(true);
  const [playing, setPlaying] = useState(false);
  const [volume, setVolume] = useState(0.6);
  const [currentTrack, setCurrentTrack] = useState(breakIds[0]);

  useEffect(() => {
    function handleKeyDown(e: any): any {
      if (e.code === "Space" && introMessage === true) {
        e.preventDefault();
        setIntroMessage(false);
      }

      if (e.code === "Space") {
        e.preventDefault();
        setPlaying((prevProps) => !prevProps);
      }

      if (e.code === "ArrowUp") {
        e.preventDefault();
        if (volume <= 0.8) {
          setVolume((prevProps) => prevProps + 0.2);
        }
      }

      if (e.code === "ArrowDown") {
        e.preventDefault();
        if (volume > 0) {
          setVolume((prevProps) => prevProps - 0.2);
        }
      }
    }

    document.addEventListener("keydown", handleKeyDown);

    return function cleanup() {
      document.removeEventListener("keydown", handleKeyDown);
    };
  }, [playing, introMessage, volume]);

  return (
    <div className={"bg-cover z-10 bg-main-wall"}>
      <Head>
        <title>Radio</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={"min-h-screen flex flex-col justify-between"}>
        <div className={"fixed hidden items-center justify-center"}>
          <YouTubeEmbed videoId={currentTrack} play={playing} volume={volume} />
        </div>

        <div className={`${styles.row} justify-between pt-3 p-8`}>
          <h1 className={styles.title}>BREAKBEAT RADIO</h1>
          <SocialMediaButtons />
        </div>

        <div className={`${styles.row} text-white align-center pb-3 p-8`}>
          {introMessage ? (
            <p className={`text-2xl`}> Press space to start playing...</p>
          ) : (
            <div>
              <div className={styles.row}>
                <MediaButtons
                  volume={volume}
                  setVolume={setVolume}
                  playing={playing}
                  setPlaying={setPlaying}
                  setCurrentTrack={setCurrentTrack}
                  breakIds={breakIds}
                />
              </div>
              <div className={`${styles.row} items-end mt-2`}>
                <NowPlaying playing={playing} />
              </div>
            </div>
          )}
        </div>
      </main>
    </div>
  );
};

export default Home;
